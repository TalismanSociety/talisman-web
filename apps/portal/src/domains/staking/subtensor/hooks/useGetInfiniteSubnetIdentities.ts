import { useInfiniteQuery } from '@tanstack/react-query'
import axios from 'axios'

import { SubnetIdentityResponse } from '../types'

const TAOSTATS_API_KEY = import.meta.env.VITE_TAOSTATS_API_KEY
const TAOSTATS_API_URL = import.meta.env.VITE_TAOSTATS_API_URL

const fetchSubnetIdentities = async ({ pageParam = 1 }) => {
  const { data } = await axios.get<SubnetIdentityResponse>(`${TAOSTATS_API_URL}/api/subnet/identity/v1`, {
    params: { page: pageParam },
    method: 'GET',
    headers: {
      Authorization: TAOSTATS_API_KEY,
      'Content-Type': 'application/json',
    },
  })
  return data
}

export function useGetInfiniteSubnetIdentities() {
  return useInfiniteQuery({
    queryKey: ['infiniteSubnetDescriptions'],
    placeholderData: {
      pages: [initialData],
      pageParams: [1],
    },
    queryFn: fetchSubnetIdentities,
    getNextPageParam: lastPage => lastPage.pagination.next_page ?? undefined,
    getPreviousPageParam: firstPage => firstPage.pagination.prev_page ?? undefined,
    initialPageParam: 1,
    staleTime: 5 * 60 * 1000, // 5 mins
    gcTime: 10 * 60 * 1000, // 10 mins
    refetchOnReconnect: true,
  })
}

